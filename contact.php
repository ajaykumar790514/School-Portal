<script src="https://cdnjs.cloudflare.com/ajax/libs/parsley.js/2.9.2/parsley.min.js"></script>
<script>
$(document).ready(function(){
    $("#myForm").parsley(); // Initialize Parsley on the form

    $("#myForm").on('submit', function(event) {
        event.preventDefault(); // Prevent default form submission behavior

        if ($(this).parsley().isValid()) {
            var formData = $(this).serialize();

            // Send an Ajax request
            $.ajax({
                type: "POST",
                url: "submit_enquiry.php", // PHP script to handle the form submission
                data: formData,
                success: function(response){
                    // Display the response in the result div
                    if(response == 'success') {
                        $(".text-success").html("Thanks for your enquiry!").fadeIn().delay(3000).fadeOut();
                        setTimeout(function(){
                            location.reload();
                        }, 3000);
                    } else {
                        $(".text-danger").html("Sorry, your enquiry could not be submitted.").fadeIn().delay(3000).fadeOut();
                    }
                }
            });
        }
    });
});
</script>
